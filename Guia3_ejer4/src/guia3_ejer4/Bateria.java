

package guia3_ejer4;


public class Bateria {

    private String bat;
    private int carga;

    public Bateria(String bat, int carga) {
        this.bat = bat;
        this.carga = carga;
    }

    public String getBat() {
        return bat;
    }

    public void setBat(String bat) {
        this.bat = bat;
    }

    public int getCarga() {
        return carga;
    }

    public void setCarga(int carga) {
        this.carga = carga;
    }
    
    
    
}
